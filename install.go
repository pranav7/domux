func patchedCodexHooks(settings map[string]any) map[string]any {
	if settings == nil {
		settings = map[string]any{}
	}
	hooks, _ := settings["hooks"].(map[string]any)
	if hooks == nil {
		hooks = map[string]any{}
		settings["hooks"] = hooks
	}
	pruneCopiedClaudeCodexHooks(hooks)
	addCommandHook(hooks, "UserPromptSubmit", "", codexDomuxHook("ai-state --agent codex CODEXING"))
	addCommandHook(hooks, "PreToolUse", "*", codexDomuxHook("ai-state --agent codex CODEXING"))
	addCommandHook(hooks, "PostToolUse", "*", codexDomuxHook("ai-state --agent codex CODEXING"))
	addCommandHook(hooks, "PermissionRequest", "*", codexDomuxHook("ai-state --agent codex WAITING"))
	addCommandHook(hooks, "Stop", "", codexDomuxHook("ai-state --agent codex --all clear"))
	return settings
}

func codexDomuxHook(args string) string {
	homeDir, err := os.UserHomeDir()
	if err != nil || homeDir == "" {
		return "domux " + args
	}
	return shellCommandPath(filepath.Join(homeDir, "bin", "domux")) + " " + args
}

func shellCommandPath(path string) string {
	if !strings.ContainsAny(path, " \t\n'\"\\$`!*?[]{}()<>|&;") {
		return path
	}
	return "'" + strings.ReplaceAll(path, "'", "'\\''") + "'"
}

func pruneCopiedClaudeCodexHooks(hooks map[string]any) {
	for event, rawEntries := range hooks {
		entries, ok := rawEntries.([]any)
		if !ok {
			continue
		}
		nextEntries := make([]any, 0, len(entries))
		for _, rawEntry := range entries {
			entry, ok := rawEntry.(map[string]any)
			if !ok {
				nextEntries = append(nextEntries, rawEntry)
				continue
			}
			rawHookList, _ := entry["hooks"].([]any)
			nextHookList := make([]any, 0, len(rawHookList))
			for _, rawHook := range rawHookList {
				hook, ok := rawHook.(map[string]any)
				if !ok {
					nextHookList = append(nextHookList, rawHook)
					continue
				}
				command, _ := hook["command"].(string)
				if isCopiedClaudeCodexHook(command) {
					continue
				}
				nextHookList = append(nextHookList, rawHook)
			}
			if len(nextHookList) == 0 {
				continue
			}
			entry["hooks"] = nextHookList
			nextEntries = append(nextEntries, entry)
		}
		if len(nextEntries) == 0 {
			delete(hooks, event)
		} else {
			hooks[event] = nextEntries
		}
	}
}

func isCopiedClaudeCodexHook(command string) bool {
	return strings.Contains(command, ".tmux-claude-") ||
		strings.Contains(command, ".tmux-workspace-$(tmux display-message") ||
		command == "domux workspace occupied" ||
		strings.Contains(command, `"$HOME/bin/domux" ai-state --agent codex`) ||
		command == "domux ai-state --agent codex CODEXING" ||
		command == "domux ai-state --agent codex WAITING" ||
		command == "domux ai-state --agent codex --all clear" ||
		command == "domux ai-state --agent codex clear"
}

func addCommandHook(hooks map[string]any, event, matcher, command string) {
	existing, _ := hooks[event].([]any)
	if hookCommandExists(existing, command) {
		return
	}
	entry := map[string]any{
		"hooks": []any{
			map[string]any{
				"type":    "command",
				"command": command,
			},
		},
	}
	if matcher != "" {
		entry["matcher"] = matcher
	}
	hooks[event] = append(existing, entry)
}

func hookCommandExists(entries []any, command string) bool {
	for _, entry := range entries {
		entryMap, ok := entry.(map[string]any)
		if !ok {
			continue
		}
		hooks, _ := entryMap["hooks"].([]any)
		for _, hook := range hooks {
			hookMap, ok := hook.(map[string]any)
			if !ok {
				continue
			}
			if hookMap["command"] == command {
				return true
			}
		}
	}
	return false
}

func backupIfExists(path string) error {
	if _, err := os.Stat(path); os.IsNotExist(err) {
		return nil
	} else if err != nil {
		return fmt.Errorf("cannot stat %s: %w", path, err)
	}
	backup := fmt.Sprintf("%s.domux-backup-%s", path, time.Now().Format("20060102150405"))
	data, err := os.ReadFile(path)
	if err != nil {
		return fmt.Errorf("cannot read %s: %w", path, err)
	}
	if err := os.WriteFile(backup, data, 0600); err != nil {
		return fmt.Errorf("cannot write backup %s: %w", backup, err)
	}
	return nil
}

func doctorCommand(args []string) error {
	if len(args) != 0 {
		return fmt.Errorf("doctor does not accept arguments")
	}
	checks := []struct {
		Name string
		OK   bool
		Info string
	}{
		{Name: "tmux", OK: commandExists("tmux"), Info: "tmux binary on PATH"},
		{Name: "domux", OK: commandExists("domux"), Info: "domux binary on PATH"},
	}
	dataDir, err := domuxDataDir()
	checks = append(checks, checkResult("data dir", err == nil && dirExists(dataDir), dataDir))
	configDir, err := domuxConfigDir()
	checks = append(checks, checkResult("config dir", err == nil && dirExists(configDir), configDir))
	tmuxFile, err := domuxConfigDir("domux.tmux")
	checks = append(checks, checkResult("tmux integration", err == nil && fileExists(tmuxFile), tmuxFile))
	homeDir, _ := os.UserHomeDir()
	claudeSettings := filepath.Join(homeDir, ".claude", "settings.json")
	checks = append(checks, checkResult("Claude settings", fileExists(claudeSettings), claudeSettings))
	codexHooks := filepath.Join(homeDir, ".codex", "hooks.json")
	checks = append(checks, checkResult("Codex hooks", fileExists(codexHooks), codexHooks))
	caffPath, err := caffeinateConfigPath()
	checks = append(checks, checkResult("caffeinate", err == nil && fileExists(caffPath), caffPath))

	for _, check := range checks {
		mark := "ok"
		if !check.OK {
			mark = "missing"
		}
		fmt.Printf("%-18s %-8s %s\n", check.Name, mark, check.Info)
	}
	return nil
}

func checkResult(name string, ok bool, info string) struct {
	Name string
	OK   bool
	Info string
} {
	return struct {
		Name string
		OK   bool
		Info string
	}{Name: name, OK: ok, Info: info}
}

func migrateCommand(args []string) error {
	fs := flag.NewFlagSet("migrate", flag.ContinueOnError)
	fs.SetOutput(os.Stderr)
	apply := fs.Bool("apply", false, "write domux session registry entries")
	if err := fs.Parse(args); err != nil {
		return err
	}
	sessions, err := currentTmuxSessions()
	if err != nil {
		return err
	}
	if len(sessions) == 0 {
		fmt.Println("no tmux sessions found")
		return nil
	}
	for _, session := range sessions {
		path, _ := tmuxPanePath(session)
		root := path
		if path != "" {
			if resolved, err := resolveRoot(path); err == nil {
				root = resolved
			}
		}
		if !*apply {
			fmt.Printf("would adopt %-20s %s\n", session, root)
			continue
		}
		if root == "" {
			continue
		}
		if _, err := setSessionRoot(session, root); err != nil {
			return err
		}
		fmt.Printf("adopted %-20s %s\n", session, root)
	}
	return nil
}

func claudeStatuslineCommand(args []string) error {
	if len(args) != 0 {
		return fmt.Errorf("claude-statusline does not accept arguments")
	}
	input, err := io.ReadAll(os.Stdin)
	if err != nil {
		return err
	}
	var payload map[string]any
	_ = json.Unmarshal(input, &payload)
	cwd := nestedString(payload, "workspace", "current_dir")
	if cwd == "" {
		cwd = stringValue(payload["cwd"])
	}
	if cwd == "" {
		cwd, _ = os.Getwd()
	}
	dirName := filepath.Base(cwd)
	model := nestedString(payload, "model", "display_name")
	model = strings.TrimPrefix(model, "Claude ")
	branch := gitBranch(cwd)
	if branch != "" {
		branch = " on \033[35m" + branch + "\033[0m"
	}
	if model != "" {
		fmt.Printf("\033[36m%s\033[0m%s \033[2m(%s)\033[0m\n", dirName, branch, model)
		return nil
	}
	fmt.Printf("\033[36m%s\033[0m%s\n", dirName, branch)
	return nil
}

func currentTmuxSessions() ([]string, error) {
	out, err := exec.Command("tmux", "list-sessions", "-F", "#{session_name}").CombinedOutput()
	if err != nil {
		msg := strings.TrimSpace(string(out))
		if msg == "" || strings.Contains(msg, "no server") || strings.Contains(msg, "failed to connect") {
			return nil, nil
		}
		return nil, fmt.Errorf("tmux list-sessions: %w: %s", err, msg)
	}
	var sessions []string
	for _, line := range strings.Split(strings.TrimSpace(string(out)), "\n") {
		if strings.TrimSpace(line) != "" {
			sessions = append(sessions, strings.TrimSpace(line))
		}
	}
	sort.Strings(sessions)
	return sessions, nil
}

func tmuxPanePath(session string) (string, error) {
	out, err := exec.Command("tmux", "display-message", "-t", session, "-p", "#{pane_current_path}").Output()
	if err != nil {
		return "", err
	}
	return strings.TrimSpace(string(out)), nil
}

func commandExists(name string) bool {
	_, err := exec.LookPath(name)
	return err == nil
}

func fileExists(path string) bool {
	_, err := os.Stat(path)
	return err == nil
}

func dirExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && info.IsDir()
}

func nestedString(payload map[string]any, keys ...string) string {
	var current any = payload
	for _, key := range keys {
		m, ok := current.(map[string]any)
		if !ok {
			return ""
		}
		current = m[key]
	}
	return stringValue(current)
}

func stringValue(value any) string {
	if s, ok := value.(string); ok {
		return s
	}
	return ""
}

func gitBranch(dir string) string {
	cmd := exec.Command("git", "-C", dir, "branch", "--show-current")
	out, err := cmd.Output()
	if err != nil {
		return ""
	}
	return strings.TrimSpace(string(out))
}
