func installCommand(args []string) error {
	if len(args) == 0 {
		fmt.Println("Usage: domux install tmux|claude|codex [--apply] | caffeinate [--full]")
		fmt.Println()
		fmt.Println("tmux/claude/codex: preview-only by default. Pass --apply to write.")
		fmt.Println("caffeinate: registers partial mode immediately. Pass --full for lid-close prevention (requires sudo).")
		return nil
	}
	switch args[0] {
	case "tmux":
		return installTmux(args[1:])
	case "claude":
		return installClaude(args[1:])
	case "codex":
		return installCodex(args[1:])
	case "caffeinate":
		return installCaffeinate(args[1:])
	default:
		return fmt.Errorf("unknown install target %q", args[0])
	}
}

func installTmux(args []string) error {
	fs := flag.NewFlagSet("install tmux", flag.ContinueOnError)
	fs.SetOutput(os.Stderr)
	apply := fs.Bool("apply", false, "write ~/.config/domux/domux.tmux")
	if err := fs.Parse(args); err != nil {
		return err
	}
	path, err := domuxConfigDir("domux.tmux")
	if err != nil {
		return err
	}
	content := generatedTmuxConfig()
	if !*apply {
		fmt.Printf("Would write %s:\n\n%s\n", path, content)
		fmt.Println("Then source it from tmux with:")
		fmt.Println("  source-file ~/.config/domux/domux.tmux")
		return nil
	}
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return fmt.Errorf("cannot create %s: %w", filepath.Dir(path), err)
	}
	if err := backupIfExists(path); err != nil {
		return err
	}
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		return fmt.Errorf("cannot write %s: %w", path, err)
	}
	fmt.Printf("wrote %s\n", path)
	fmt.Println("Add this to ~/.tmux.conf when ready:")
	fmt.Println("  source-file ~/.config/domux/domux.tmux")
	return nil
}

func generatedTmuxConfig() string {
	return strings.TrimSpace(`
# Generated by domux. Source this file from ~/.tmux.conf.

bind-key s display-popup -E -w 60% -h 50% "$HOME/bin/domux sessions"
bind-key t display-popup -E -w 60% -h 50% -d "#{pane_current_path}" "$HOME/bin/domux"
bind-key u display-popup -E -w 50% -h 30% "$HOME/bin/domux commands"

bind-key i run-shell '$HOME/bin/domux ai-state --session "#{session_name}" --pane "#{window_index}_#{pane_index}" toggle'
bind-key v run-shell '$HOME/bin/domux server --session "#{session_name}" toggle'
bind-key f run-shell '$HOME/bin/domux clear-state' \; display-message "domux session state cleared"

bind-key N command-prompt -p "Session label:" "run-shell '$HOME/bin/domux label --session \"#{session_name}\" set \"%%\"'"
bind-key n run-shell '$HOME/bin/domux label --session "#{session_name}" clear' \; display-message "domux label cleared"

set-option -g status-right '#($HOME/bin/domux status "#{session_name}" "#{pane_current_path}") #[fg=#{@tertiary-bg},bg=#{status-bg}]#[fg=#{status-fg},bg=#{@tertiary-bg}] %H:%M #[fg=#{@tertiary-bg},bg=#{status-bg}] #[fg=#{@quaternary-bg},bg=#{status-bg}]#[fg=#{status-fg},bg=#{@quaternary-bg}] %d-%b-%y   '
`) + "\n"
}

func installClaude(args []string) error {
	fs := flag.NewFlagSet("install claude", flag.ContinueOnError)
	fs.SetOutput(os.Stderr)
	apply := fs.Bool("apply", false, "patch ~/.claude/settings.json")
	if err := fs.Parse(args); err != nil {
		return err
	}
	homeDir, err := os.UserHomeDir()
	if err != nil {
		return fmt.Errorf("cannot get home directory: %w", err)
	}
	path := filepath.Join(homeDir, ".claude", "settings.json")
	next, err := patchedClaudeSettings(path)
	if err != nil {
		return err
	}
	cmdPath := filepath.Join(homeDir, ".claude", "commands", claudeStartTaskFile)
	if !*apply {
		data, _ := json.MarshalIndent(next, "", "  ")
		fmt.Printf("Would patch %s with domux hooks/statusLine:\n\n%s\n", path, data)
		fmt.Printf("\nWould write %s (the /start-task slash command).\n", cmdPath)
		return nil
	}
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return fmt.Errorf("cannot create %s: %w", filepath.Dir(path), err)
	}
	if err := backupIfExists(path); err != nil {
		return err
	}
	data, err := json.MarshalIndent(next, "", "  ")
	if err != nil {
		return err
	}
	data = append(data, '\n')
	if err := os.WriteFile(path, data, 0600); err != nil {
		return fmt.Errorf("cannot write %s: %w", path, err)
	}
	fmt.Printf("patched %s\n", path)
	if err := writeClaudeCommand(cmdPath, claudeStartTaskCommand); err != nil {
		return err
	}
	return nil
}

func writeClaudeCommand(path, content string) error {
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		return fmt.Errorf("cannot create %s: %w", filepath.Dir(path), err)
	}
	existing, err := os.ReadFile(path)
	if err == nil && string(existing) == content {
		return nil
	}
	if err == nil {
		if err := backupIfExists(path); err != nil {
			return err
		}
	} else if !os.IsNotExist(err) {
		return fmt.Errorf("cannot read %s: %w", path, err)
	}
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		return fmt.Errorf("cannot write %s: %w", path, err)
	}
	fmt.Printf("wrote %s\n", path)
	return nil
}

func patchedClaudeSettings(path string) (map[string]any, error) {
	settings := map[string]any{}
	data, err := os.ReadFile(path)
	if err == nil && len(strings.TrimSpace(string(data))) > 0 {
		if err := json.Unmarshal(data, &settings); err != nil {
			return nil, fmt.Errorf("cannot parse %s: %w", path, err)
		}
	} else if err != nil && !os.IsNotExist(err) {
		return nil, fmt.Errorf("cannot read %s: %w", path, err)
	}

	hooks, _ := settings["hooks"].(map[string]any)
	if hooks == nil {
		hooks = map[string]any{}
		settings["hooks"] = hooks
	}
	addCommandHook(hooks, "SessionStart", "", "domux workspace occupied")
	addCommandHook(hooks, "SessionEnd", "", "domux workspace free && domux ai-state clear")
	addCommandHook(hooks, "PreToolUse", "*", "domux ai-state CLAUDING")
	addCommandHook(hooks, "UserPromptSubmit", "", "domux ai-state CLAUDING")
	addCommandHook(hooks, "Notification", "", "domux ai-state WAITING")
	addCommandHook(hooks, "PreCompact", "", "domux ai-state --agent claude COMPACTING")
	addCommandHook(hooks, "PostCompact", "", "domux ai-state --agent claude CLAUDING")
	addCommandHook(hooks, "Stop", "", "domux ai-state clear")

	// Only set statusLine if the user hasn't configured one. Overwriting
	// a custom statusLine (custom shell script, etc.) without warning is
	// the kind of side effect that doesn't belong in an idempotent patch.
	if _, exists := settings["statusLine"]; !exists {
		settings["statusLine"] = map[string]any{
			"type":    "command",
			"command": "domux claude-statusline",
		}
	}
	return settings, nil
}

